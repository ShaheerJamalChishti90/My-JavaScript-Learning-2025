// Converting the variable datatype from A to B is called the typecasting
// eg: converting a string into a number is called the typecasting

let a = "5"
let b = 5

let z = Number.parseInt(a)

console.log(z + b)

let c = "5.12"
let d = 5.12

let e = Number.parseFloat(c)

console.log(e + d)

